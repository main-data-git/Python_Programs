# What is vectorization and why is it faster than loops in NumPy ?


#In NumPy, vectorization refers to performing operations on entire arrays (vectors, matrices, etc.) without writing explicit Python loops. Instead, NumPy uses fast, low-level implementations (written in C) to apply operations element-wise or with broadcasting.

#Why vectorization matters

#Speed: Vectorized operations run much faster than Python loops.

#Cleaner code: No need for for loops to iterate element by element.

#Leverages SIMD + optimized C routines under the hood



